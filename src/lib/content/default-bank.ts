import type { AdminSettings, PhotoMission, Question } from "@/lib/types";

function buildQuestion(
  id: string,
  title: string,
  prompt: string,
  options: [string, string, string],
): Question {
  return {
    type: "mcq",
    id,
    title,
    prompt,
    options: options.map((label, index) => ({
      id: (["a", "b", "c"] as const)[index],
      label,
    })),
    correctOptionId: "a",
    correctOptionIndex: 0,
    basePoints: 100,
  };
}

function buildMission(
  id: string,
  title: string,
  prompt: string,
  options?: { isFinal?: boolean; basePoints?: number },
): PhotoMission {
  return {
    type: "photo",
    id,
    title,
    prompt,
    basePoints: options?.basePoints ?? 250,
    isFinal: options?.isFinal,
  };
}

